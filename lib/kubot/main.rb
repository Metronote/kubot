module Kubot
  class Main < Clamp::Command

    option "--loud", :flag, "say it loud"
    option ["-n", "--iterations"], "N", "say it N times", :default => 1 do |s|
      Integer(s)
    end

    parameter "WORDS ...", "the thing to say", :attribute_name => :words

    def execute
      Kubot::Bot.run
    end

  end
end
